package com.ylife.form.model;

import java.math.BigDecimal;
import java.util.Date;

/**
 * Created by Administrator on 2016/8/8.
 */
public class UcoinGrandForm {

    private  String idCard;

    private String fullname;

    private Long grandAmount;

    private BigDecimal price;

    private BigDecimal salePrice;

    private BigDecimal marketPrice;

    private Date createTime;

    private String grandEnterprise;

    public String getIdCard() {
        return idCard;
    }

    public void setIdCard(String idCard) {
        this.idCard = idCard;
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    public Long getGrandAmount() {
        return grandAmount;
    }

    public void setGrandAmount(Long grandAmount) {
        this.grandAmount = grandAmount;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }

    public BigDecimal getSalePrice() {
        return salePrice;
    }

    public void setSalePrice(BigDecimal salePrice) {
        this.salePrice = salePrice;
    }

    public BigDecimal getMarketPrice() {
        return marketPrice;
    }

    public void setMarketPrice(BigDecimal marketPrice) {
        this.marketPrice = marketPrice;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public String getGrandEnterprise() {
        return grandEnterprise;
    }

    public void setGrandEnterprise(String grandEnterprise) {
        this.grandEnterprise = grandEnterprise;
    }
}
